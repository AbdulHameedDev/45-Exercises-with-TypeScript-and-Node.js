// Declare an array named "names" of type string[], containing a list of names
let names: string[] = ["Hamza", "Usman", "Ayesha", "Areeba"];

// Iterate over each element of the "names" array using the forEach method
// For each name in the array, log a greeting message to the console using string interpolation
names.forEach(name => {
  console.log(`Assalam o Alaikum ${name}, umeed karta ho ap Kheriyat sy hongy!`);
});
